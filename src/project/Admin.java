    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {int row = adtable.getSelectedRow();
        int UserID= Integer.parseInt(tableModel.getValueAt(row, 0).toString());
        String userName = tableModel.getValueAt(row, 1).toString();
        String Pass = tableModel.getValueAt(row, 2).toString();
        int roleID = Integer.parseInt(tableModel.getValueAt(row, 3).toString());
        try {
            switch( roleID){
                case 1:
                case 2:    
                        this.dispose();
                        customer c= new customer(UserID,userName,Pass,roleID);
                        UpdateCustomer uc = new UpdateCustomer();
                        uc.updateCustomer(c);
                        uc.setVisible(true);
                        break;
                case 3:
                case 4:
                    this.dispose();
                    customer c1=new customer(UserID,userName,Pass,roleID);
                    UpdateUser u= new UpdateUser();
                    u.updateUser(c1);
                    u.setVisible(true);
                    break;
        }
        } catch (SQLException ex) {
            Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
        }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null,"Please select user","Warning",JOptionPane.INFORMATION_MESSAGE );
            Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, e);
        }
       
    }//GEN-LAST:event_jButton3ActionPerformed

