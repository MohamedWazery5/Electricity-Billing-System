    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            customer c= new customer();
            int row = adtable.getSelectedRow();
            int userID = Integer.parseInt(tableModel.getValueAt(row, 0).toString());
            int roleID = Integer.parseInt(tableModel.getValueAt(row, 3).toString());
            c.deleteUser(userID);
            JOptionPane.showMessageDialog(null, "Deleted Successfully", "Delete user", JOptionPane.INFORMATION_MESSAGE);
            tableModel.removeRow(row);
            c.db.CloseConnection();
        } catch (SQLException ex) {
            Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
        }
            

    }//GEN-LAST:event_jButton1ActionPerformed

